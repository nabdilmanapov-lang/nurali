#Specify a Variable Type

#Integers
x = int(3)   # x will be 3
y = int(54.88) # y will be 54
z = int("82") # z will be 82

#Floats
x = float(6)     # x will be 6.0
y = float(43.26)   # y will be 43.26
z = float("23")   # z will be 23.0
w = float("14.66") # w will be 14.66

#Strings
x = str("number1") # x will be 'number1'
y = str(468)    # y will be '468'
z = str(44.0)  # z will be '44.0'
